    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                                         
    try {
        String text = jTextField1.getText().trim();

        // Verifica se o campo está vazio após remover espaços
        if (text.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, insira o comprimento da senha.");
            return;
        }

        // Verifica se o texto contém apenas dígitos
        if (!text.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Por favor, insira um número válido.");
            return;
        }

        int comprimento = Integer.parseInt(text);
        if (comprimento <= 0 || comprimento > 128) {
            JOptionPane.showMessageDialog(this, "Por favor, insira um comprimento válido (número positivo, até 128 caracteres).");
            return;
        }

        // Checa as opções selecionadas para gerar a senha
        boolean incluirMaiusculas = jCheckBox1.isSelected();
        boolean incluirMinusculas = jCheckBox2.isSelected();
        boolean incluirNumeros = jCheckBox3.isSelected();
        boolean incluirEspeciais = jCheckBox4.isSelected();

        if (!incluirMaiusculas && !incluirMinusculas && !incluirNumeros && !incluirEspeciais) {
            JOptionPane.showMessageDialog(this, "Selecione pelo menos um tipo de caractere.");
            return;
        }

        // Gera a senha com base nas opções
        String senhaGerada = generatePassword(comprimento, incluirMaiusculas, incluirMinusculas, incluirNumeros, incluirEspeciais);
        jLabel1.setText(senhaGerada);

    } catch (HeadlessException | NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

