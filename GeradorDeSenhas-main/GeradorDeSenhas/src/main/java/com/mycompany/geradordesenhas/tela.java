     private String generatePassword(int length, boolean includeUpper, boolean includeLower, boolean includeNumbers, boolean includeSpecial) {
        final String upper = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
        final String lower = "abcdefghijklmnopqrstuvwxyz";
        final String numbers = "0123456789";
        final String special = "!@#$%^&*()_-+=[{]}|;:'\",<.>/?";

        StringBuilder characterPool = new StringBuilder();
        if (includeUpper) characterPool.append(upper);
        if (includeLower) characterPool.append(lower);
        if (includeNumbers) characterPool.append(numbers);
        if (includeSpecial) characterPool.append(special);

        Random random = new Random();
        StringBuilder password = new StringBuilder();
        for (int i = 0; i < length; i++) {
            password.append(characterPool.charAt(random.nextInt(characterPool.length())));
        }

        return password.toString();
    }

